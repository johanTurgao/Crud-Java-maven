    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

    JFileChooser selectorArchivos = new JFileChooser();
    FileNameExtensionFilter filtroImagen=new FileNameExtensionFilter("*.jpg, *.png, *.gif","jpg","png","gif");
    selectorArchivos.setFileFilter(filtroImagen);
    selectorArchivos.showOpenDialog(this);
    File archivo = selectorArchivos.getSelectedFile();
    
    if ((archivo == null) || (archivo.getName().equals(""))) {
        JOptionPane.showMessageDialog(this, "Inválido", "Nombre de archivo inválido", JOptionPane.ERROR_MESSAGE);
    }
    try {
        Entrada = archivo.getAbsolutePath();
        Entrada=Entrada.replace("\\", "/");
        Salida = Consulta.Codificar_Imagen(Entrada);
    } catch (IOException ex) {
        Logger.getLogger(Interfase.class.getName()).log(Level.SEVERE, null, ex);
    }
    Foto.setIcon(Consulta.Deodificar_Imagen(Salida,100));
    }//GEN-LAST:event_jButton1MouseClicked

    private void crearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_crearMouseClicked
        try{
            Consulta.Crear(usuario.getText(), contrasenna.getText(), Integer.valueOf(nivel.getText()), Salida);
            Limpiar();
            lista();
        }
        catch(IOException | NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Inválido", "No se pudo crear", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_crearMouseClicked

    private void editarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editarMouseClicked
        try{
            Consulta.Editar(Integer.valueOf(id.getText()),usuario.getText(), contrasenna.getText(), Integer.valueOf(nivel.getText()), Salida);
            Limpiar();
            lista();
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Inválido", "No se pudo Editar, por tanto se creo", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException ex) {
            Logger.getLogger(Interfase.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_editarMouseClicked

    private void borrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_borrarMouseClicked
        try{
            JOptionPane.showMessageDialog(this, "Atencion", "Se borrara un registro", JOptionPane.WARNING_MESSAGE);
            Consulta.Borrar(Integer.valueOf(id.getText()));
            Limpiar();
            lista();
        }
        catch(HeadlessException | IOException | NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Inválido", "No se pudo Editar, por tanto se creo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_borrarMouseClicked

    private void buscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscarMouseClicked
        try{
            Login obj = Consulta.BuscarId(Integer.valueOf(id.getText()));
            usuario.setText(obj.getUsuario());
            contrasenna.setText(obj.getContraseña());
            nivel.setText(String.valueOf(obj.getNivel()));
            Salida = obj.getFoto();
            Foto.setIcon(Consulta.Deodificar_Imagen(obj.getFoto(),100));
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Inválido", "No se pudo encontrar", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_buscarMouseClicked

