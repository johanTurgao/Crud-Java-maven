    private void lista(){
        int i=0;
        tabla.setDefaultRenderer(Object.class,new IconCellRenderer());
        tabla.setRowHeight(30);
        for(Login a:Consulta.BuscarTodo()){            
            ImageIcon perfil = Consulta.Deodificar_Imagen(a.getFoto(), 30);
            tabla.setValueAt(a.getId(), i, 0);
            tabla.setValueAt(a.getUsuario(), i, 1);
            tabla.setValueAt(a.getContraseña(), i, 2);
            tabla.setValueAt(a.getNivel(), i, 3);
            tabla.setValueAt(new JLabel(perfil), i, 4);
            i++;
        }
    }

